/**
 * Função global para cadastrar um novo ramal na lista mestra.
 * Converte automaticamente as strings legíveis para Base64 antes do push.
 * * @param {string} setor - Nome do setor (ex: "TI", "RH")
 * @param {string} nome - Nome do funcionário ou local (ex: "JOÃO SILVA")
 * @param {string|number} ramal - Número do ramal (ex: "8555" ou 8555)
 */
window.cadastrarNovoRamal = function(setor, nome, ramal) {
    if (!setor || !nome || !ramal) {
        console.error("❌ Erro: Todos os campos (setor, nome, ramal) são obrigatórios.");
        return null;
    }

    // Tratamento nativo para caracteres acentuados especiais com btoa()
    const stringToBase64 = (str) => {
        return btoa(unescape(encodeURIComponent(str.toUpperCase().trim())));
    };

    const novoItem = {
        setor: stringToBase64(setor),
        nome: stringToBase64(nome),
        ramal: stringToBase64(ramal.toString()),
        id: self.crypto.randomUUID()
    };

    window.LISTA_MESTRA.push(novoItem);
    console.log(`✅ Ramal de "${nome.toUpperCase()}" cadastrado e empurrado para a LISTA_MESTRA.`);
    return novoItem;
};
